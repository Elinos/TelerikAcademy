using System;

class FloatAndDouble
{
    static void Main()
    {
        double realNumber1 = 34.567839023;
        Console.WriteLine("{0} must be double", realNumber1);
        float realNumber2 = 12.345f;
        Console.WriteLine("{0} must be float", realNumber2);
        double realNumber3 = 8923.1234857;
        Console.WriteLine("{0} must be double", realNumber3);
        float realNumber4 = 3456.091f;
        Console.WriteLine("{0} must be float", realNumber4);
    }
}

